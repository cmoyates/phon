package ca.phon.ipa.parser.exceptions;

public class HangingStressException extends IPAParserException {

	private static final long serialVersionUID = -3718601542453319222L;

	public HangingStressException() {
		super();
	}

	public HangingStressException(String message, Throwable cause) {
		super(message, cause);
	}

	public HangingStressException(String message) {
		super(message);
	}

	public HangingStressException(Throwable cause) {
		super(cause);
	}

}
