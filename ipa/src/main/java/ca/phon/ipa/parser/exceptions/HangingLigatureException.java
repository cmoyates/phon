package ca.phon.ipa.parser.exceptions;

public class HangingLigatureException extends IPAParserException {

	private static final long serialVersionUID = -3718601542453319222L;

	public HangingLigatureException() {
		super();
	}

	public HangingLigatureException(String message, Throwable cause) {
		super(message, cause);
	}

	public HangingLigatureException(String message) {
		super(message);
	}

	public HangingLigatureException(Throwable cause) {
		super(cause);
	}

}
