package ca.phon.app.session.editor.view.ipa_lookup;

import ca.phon.app.session.editor.*;
import ca.phon.plugin.*;

@PhonPlugin
@EditorViewInfo(name=IPALookupView.VIEW_NAME, icon=IPALookupView.VIEW_ICON, category=EditorViewCategory.UTILITIES)
public class IPALookupViewExtension implements IPluginExtensionPoint<EditorView> {

	@Override
	public Class<?> getExtensionType() {
		return EditorView.class;
	}

	@Override
	public IPluginExtensionFactory<EditorView> getFactory() {
		return factory;
	}
	
	private final IPluginExtensionFactory<EditorView> factory = new IPluginExtensionFactory<EditorView>() {
		
		@Override
		public EditorView createObject(Object... args) {
			final SessionEditor editor = (SessionEditor)args[0];
			return new IPALookupView(editor);
		}
	};

}
